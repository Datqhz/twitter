class MyUser{
  String? username;
  bool? status;
  String? displayName;
  String? bio;
  String? location;
  DateTime? birthDate;
  String? phone;
  String? email;

  MyUser({required this.username,   required this.status, required this.displayName,
    required this.bio,  required this.location, required this.birthDate, required this.phone, required this.email});
}